    private void cetakPesanan(){
        for(int i=0; i<4; i++){
            for(int j=0; j<3; j++){
                listPesanan.setValueAt(this.pesanan[i][j], j, i);
            }
        }
    }
    
    private String printStruk(){
        return "Nama Kasir \t: "+ this.kasir
                + "\nNomer Meja \t: " + nomerMeja.getText() 
                + "\nPPN \t\t: " + this.pajak + "%"
                + "\nTotal Pesanan \t: " + bayarPesanan.getText()
                + "\nUang Kembali \t: " + uangKembali.getText()
                + "\nTotal Pembayaran \t: " + totalPembayaran.getText();
    }

