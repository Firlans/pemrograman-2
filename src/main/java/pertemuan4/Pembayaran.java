    private void cetakButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakButtonActionPerformed
        // TODO add your handling code here:
        int kembalian = Integer.parseInt(uangPembayaran.getText()) - Integer.parseInt(bayarPesanan.getText()); 
        int total = Integer.parseInt(bayarPesanan.getText()) + (this.pajak * Integer.parseInt(bayarPesanan.getText()) / 100);
        uangKembali.setText(Integer.toString(kembalian));
        totalPembayaran.setText(Integer.toString(total));
        struk.setText(printStruk());
    }//GEN-LAST:event_cetakButtonActionPerformed

